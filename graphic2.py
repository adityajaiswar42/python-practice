import turtle
t=turtle.Turtle()
turtle.bgcolor("black")
t.speed(1)
turtle.shapesize(2)
t.color("cyan","green")
t.fillcolor("orange")
t.begin_fill
t.forward(50)
t.left(45)
t.forward(50)
t.right(45)
t.forward(50)
t.right(45)
t.forward(50)
t.left(45)
t.forward(50)
t.right(90)
t.forward(30)
t.pendown(30)
t.forward(50)
t.penup(30)
t.forward(50)
t.end_fill()
turtle.done()
